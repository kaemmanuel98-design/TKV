export function getVerseOfDay(lang = 'fr') {
  const code = lang?.split('-')[0] || 'fr';
  const dayIndex = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000
  ) % dailyVerses.length;
  const verse = dailyVerses[dayIndex];
  return {
    ref: verse.ref[code] || verse.ref.fr,
    text: verse.text[code] || verse.text.fr,
  };
}
