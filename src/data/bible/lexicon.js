export function getLexiconEntry(strongId, lang = 'fr') {
  const entry = STRONG_LEXICON[strongId];
  if (!entry) return null;
  const l = lang.split('-')[0];
  return {
    id: strongId,
    lemma: entry.lemma[l] || entry.lemma.fr || entry.lemma.en,
    gloss: entry.gloss[l] || entry.gloss.fr || entry.gloss.en,
    isGreek: strongId.startsWith('G'),
  };
}
